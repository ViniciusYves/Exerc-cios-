# Faça um algoritmo que leia o salário de um funcionário e mostre seu novo valor com um reajuste de 15%

salario = float (input ("Insira o valor de seu salário atual: "))
acrescimo = float ((salario*15)/100)
print ("Quando somado ao acréscimo de 15% seu salário possuí o valor de R$ {}".format (acrescimo+salario))


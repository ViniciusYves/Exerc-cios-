# Crie um algoritomo que leia um número e mostre o seu dobro, triplo e raiz quadrada

print ("O presente algoritmo tem como objetivo apresentar o dobro do número inserido e sua raiz quadrada")
a = int(input("Insira um número inteiro para iniciar"))
print ("O dobro de {} é {}, seu triplo é {} e sua raiz quadrada possuí o valor de {:.2f}!".format(a,a*2,a*3,a**(1/2)))



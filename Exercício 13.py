# Faça um algoritmo que leia o preço de um produto e mostre seu novo preço com um acréscimo de 5% de desconto

produto = float (input ("Digite o valor do seu produto: R$ "))
desconto = float ((produto*5)/100) # preço - 5%
print ("O produto informado possuí um desconto de 5% totalizando R${}".format(produto-desconto))
